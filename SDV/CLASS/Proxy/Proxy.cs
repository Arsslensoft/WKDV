        private void onNetworkPrefsChange(object sender, PrefChangeEventArgs oPCE)
        {
            if (oPCE.PrefName == "SDVP.network.sockets.ClientReadBufferSize")
            {
                ClientChatter._cbClientReadBuffer = SDVPApplication.Prefs.GetInt32Pref("SDVP.network.sockets.ClientReadBufferSize", 0x2000);
            }
            if (oPCE.PrefName == "SDVP.network.sockets.ServerReadBufferSize")
            {
                ServerChatter._cbServerReadBuffer = SDVPApplication.Prefs.GetInt32Pref("SDVP.network.sockets.ServerReadBufferSize", 0x8000);
            }
            if (oPCE.PrefName == "SDVP.network.egress.ip")
            {
                this.SetDefaultEgressEndPoint(oPCE.ValueString);
            }
            else if (oPCE.PrefName == "SDVP.network.https.NoDecryptionHosts")
            {
                Browser.SetNoDecryptList(oPCE.ValueString);
            }
            else if (oPCE.PrefName == "SDVP.network.proxy.RegistrationHostName")
            {
                Browser.sSDVPListenHostPort = SDVPApplication.Prefs.GetStringPref("SDVP.network.proxy.RegistrationHostName", "127.0.0.1") + ":" + Browser.ListenPort.ToString();
            }
        }

