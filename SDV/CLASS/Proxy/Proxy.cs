        internal Proxy(bool bIsPrimary)
        {
            if (bIsPrimary)
            {
                try
                {
                    NetworkChange.NetworkAvailabilityChanged += new NetworkAvailabilityChangedEventHandler(this.NetworkChange_NetworkAvailabilityChanged);
                    NetworkChange.NetworkAddressChanged += new NetworkAddressChangedEventHandler(this.NetworkChange_NetworkAddressChanged);
                }
                catch (Exception)
                {
                }
                try
                {
                    this.watcherPrefNotify = new PreferenceBag.PrefWatcher?(SDVPApplication.Prefs.AddWatcher("SDVP.network", new EventHandler<PrefChangeEventArgs>(this.onNetworkPrefsChange)));
                    this.SetDefaultEgressEndPoint(SDVPApplication.Prefs["SDVP.network.egress.ip"]);
                    Browser.SetNoDecryptList(SDVPApplication.Prefs["SDVP.network.https.NoDecryptionHosts"]);
                    Browser.sSDVPListenHostPort = SDVPApplication.Prefs.GetStringPref("SDVP.network.proxy.RegistrationHostName", "127.0.0.1") + ":" + Browser.ListenPort.ToString();
                    ClientChatter._cbClientReadBuffer = SDVPApplication.Prefs.GetInt32Pref("SDVP.network.sockets.ClientReadBufferSize", 0x2000);
                    ServerChatter._cbServerReadBuffer = SDVPApplication.Prefs.GetInt32Pref("SDVP.network.sockets.ServerReadBufferSize", 0x8000);
                }
                catch (Exception)
                {
                }
            }
        }

        private void _DetermineGatewayIPEndPoints()
        {
            this._ipepHttpGateway = GetFirstRespondingEndpoint(this.piPrior.sHttpProxy);
            if (this.piPrior.sHttpsProxy == this.piPrior.sHttpProxy)
            {
                this._ipepHttpsGateway = this._ipepHttpGateway;
            }
            else
            {
                this._ipepHttpsGateway = GetFirstRespondingEndpoint(this.piPrior.sHttpsProxy);
            }
            if (this.piPrior.sFtpProxy == this.piPrior.sHttpProxy)
            {
                this._ipepFtpGateway = this._ipepHttpGateway;
            }
            else
            {
                this._ipepFtpGateway = GetFirstRespondingEndpoint(this.piPrior.sFtpProxy);
            }
        }

